class EspConfigLoader {
    private int x;
    private int y;

    public EspConfigLoader(int x, int y) {
        this.x = x;
        this.y = y;
    }

    @Override
    public String toString() {
        return "EspConfigLoader{" +
                "x=" + x +
                ", y=" + y +
                '}';
    }
}
